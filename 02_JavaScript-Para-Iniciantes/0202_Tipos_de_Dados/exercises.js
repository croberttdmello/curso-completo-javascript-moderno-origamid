// Declare uma variável contendo uma string
let phrase = 'A vida é uma costura, feita com a linha do tempo.';
console.log(phrase);

// Declare uma variável contendo um número dentro de uma string
let age = `Eu tenho ${25} anos.`;
console.log(age);

// Declare uma variável com a sua idade
let myAge = 25;
console.log(myAge);

// Declare duas variáveis, uma com seu nome e outra com seu sobrenome e some as mesmas
let name = 'Carlos Robert';
let surname = 'Dias Melo';
console.log(`${name + ' ' + surname}`);

// Coloque a seguinte frase em uma variável: It's time
let time = "It's time.";
console.log(time);

// Verifique o tipo da variável que contém o seu nome
console.log(typeof name);



